#Simples atribuição/definição da variavel
saldo = 500
print(saldo) #printa 500

#Atribuição com adição
saldo += 200
print(saldo) #printa 700

#Atribuição com subtração
saldo -= 600
print(saldo) #printa 100

#Atribução com multiplicação
saldo *= 2
print(saldo) #printa 200

#atribução com divisão
saldo //= 4 # somente uma barra '/' retorna um float, '//' retorna um int
print(saldo) #printa 50

#atribuição com módulo
saldo %= 40
print(saldo) #printa 10

#atribuição com exponenciação
saldo **= 2
print(saldo) #printa 100