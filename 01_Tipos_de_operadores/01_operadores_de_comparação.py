saldo = input("Digite seu saldo:")
saque = input("Digite o valor de saque:")

print(f"saque é maior que saldo? {saque > saldo}")
print(f"saque é menor que saldo? {saque < saldo}")
print(f"saque é igual ao saldo? {saque == saldo}")
print(f"saque é diferente de saldo? {saque != saldo}")
print(f"saque é maior ou igual ao saldo? {saque >= saldo}")
print(f"saque é menor ou igual ao saldo? {saque <= saldo}")